package de.l3s.interwebj.connector.ted;


public class TedVideo {
	public String speaker;
	public String location;
	public String duration;
	public String description;
	public String combinedDescription;
	public String talk;
	public String title;
	public String date;
	public String totalviews;
	public String keywords;
	public String transcript;
	public String value;
	public String thumbnail;
	
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String captionString;
	public String getSpeaker() {
		return speaker;
	}
	public void setSpeaker(String speaker) {
		this.speaker = speaker;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCombinedDescription() {
		return combinedDescription;
	}
	public void setCombinedDescription(String combinedDescription) {
		this.combinedDescription = combinedDescription;
	}
	public String getTalk() {
		return talk;
	}
	public void setTalk(String talk) {
		this.talk = talk;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTotalviews() {
		return totalviews;
	}
	public void setTotalviews(String totalviews) {
		this.totalviews = totalviews;
	}
	public String getKeywords() {
		return keywords;
	}
	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}
	public String getTranscript() {
		return transcript;
	}
	public void setTranscript(String transcript) {
		this.transcript = transcript;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	} 
	public String getCaptionString() {
		return captionString;
	}
	public void setCaptionString(String captionString) {
		this.captionString = captionString;
	}
	
	
}
